package Models;

public class Quiz {
    private Integer numTurma;
    private String conteudoDescricao;

    public Integer getNumTurma() {
        return numTurma;
    }

    public void setNumTurma(Integer numTurma) {
        this.numTurma = numTurma;
    }

    public String getConteudoDescricao() {
        return conteudoDescricao;
    }

    public void setConteudoDescricao(String conteudoDescricao) {
        this.conteudoDescricao = conteudoDescricao;
    }
}
